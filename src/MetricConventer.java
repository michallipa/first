public class MetricConventer {
    double metersToCm(double meters){
        return meters * 100;
    }
    double metersToMm(double meters) {
        return meters * 1000;
    }
    double cmToMeters(double cm){
        return cm /100;
    }
    double nmToMeters(double nm){
        return nm / 1000;
    }
}
